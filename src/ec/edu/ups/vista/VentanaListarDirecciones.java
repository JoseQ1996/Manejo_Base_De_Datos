    /**
     * Llena los datos de la tabla con las direcciones de la base de datos dependiendo su cedula
     */
     public static  void llenarTabla(){
         ControladorDirecciondb controladorDirecciondb=new ControladorDirecciondb();
         String cedula=VentanaListarPersona.cedula;
         listaDirecciones=controladorDirecciondb.listaDirecciones();
         for (Direccion d:listaDirecciones ) {
             if(cedula.equals(d.getCedula_per())){
                Object[] datos = {d.getCodigo(),
                d.getCodigo(),
                d.getCalle_principal(),
                d.getCalle_secundaria(),
                d.getNumero()};
                modelo.addRow(datos);
             }
        }
    }
